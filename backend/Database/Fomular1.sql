-- Tạo CSDL
CREATE DATABASE Formula1;

-- Sử dụng CSDL vừa tạo
USE Formula1;


 /*Tao bang */
CREATE TABLE Teams(
    TeamID INT PRIMARY KEY, /* ID đội đua */
    TeamName VARCHAR(255) NOT NULL, /* Tên đội đua */
    TeamPic VARCHAR(255) NOT NULL, /* Ảnh đội đua */
    FounderYear INT NOT NULL, /* Năm thành lập */
    Base VARCHAR(255) NOT NULL, /* Trụ sở */
    TeamChief VARCHAR(255) NOT NULL, /* Trưởng nhóm */
    WorldChampionship INT NOT NULL /* Số lần vô địch thế giới */
);

CREATE TABLE Drivers(
    DriverID INT PRIMARY KEY NOT NULL, /* ID tay đua */
    DriverName VARCHAR(255) NOT NULL, /* Tên tay đua */
    DriverBirth DATE NOT NULL, /* Ngày sinh */
    PlaceOfBirth VARCHAR(255) NOT NULL, /* Nơi sinh */
    DriverPic VARCHAR(255) NOT NULL, /* Ảnh của tay đua */
    DriverNumber INT NOT NULL, /* Số áo của tay đua */
    TeamID INT NOT NULL, 
    Nationality VARCHAR(100) NOT NULL, /* Quốc tịch */
    FlagPic VARCHAR(255) NOT NULL, /* Ảnh cờ quốc gia */
    Podium INT NOT NULL, /* Số lần vào top 3 */
    Points DECIMAL(10,2) NOT NULL, /* Điểm số trong sự nghiệp */
    GrandsPrixEntered INT NOT NULL, /* Số lần tham gia trận đấu */
	DriverPicTwo VARCHAR(255) NOT NULL, /* Ảnh chân dung của tay đua */
    /* Thêm khóa ngoại */
    FOREIGN KEY (TeamID) REFERENCES Teams(TeamID) 
);

CREATE TABLE RaceTracks(
    RaceTrackID INT PRIMARY KEY NOT NULL, /* ID trường đua */
    RaceTrackName VARCHAR(255) NOT NULL, /* Tên trường đua */
    RaceTrackPic VARCHAR(255) NOT NULL, /* Ảnh trường đua */
    Location VARCHAR(255) NOT NULL, /* Địa điểm trường đua */
    Length DECIMAL(10,2) NOT NULL /* Chiều dài trường đua */
);

CREATE TABLE Races(
    RaceID INT PRIMARY KEY NOT NULL, /* ID trận đua */
    RaceName VARCHAR(255) NOT NULL, /* Tên trận đấu */
    RaceTrackID INT NOT NULL, /* Trận đua tổ chức ở trường đua nào */
    Date DATE NOT NULL, /* Thời gian diễn ra */
    NumberOfLaps INT NOT NULL, /* Số vòng đua */
    /* Thêm khóa ngoại */
    FOREIGN KEY (RaceTrackID) REFERENCES RaceTracks(RaceTrackID) 
);

CREATE TABLE DetailResult(
    RaceID INT NOT NULL,
    DriverID INT NOT NULL,
    Position INT NOT NULL, /* Thứ hạng */
    LapsCompleted INT NOT NULL, /* Số vòng đã hoàn thành */
    TimeOrRetired VARCHAR(255) NOT NULL, /* Thời gian hoàn thành hoặc bỏ cuộc */
    /* Thêm khóa ngoại */
    FOREIGN KEY (RaceID) REFERENCES Races(RaceID),
    FOREIGN KEY (DriverID) REFERENCES Drivers(DriverID) 
);


